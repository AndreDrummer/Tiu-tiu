  static AutoSizeText autoSizeText8(
    String? text, {
    TextDecoration? textDecoration,
    TextOverflow? textOverflow,
    FontWeight? fontWeight,
    FontStyle? fontStyle,
    TextAlign? textAlign,
    double fontSize = 8,
    int? maxLines,
    Color? color,
  }) {
    return AutoSizeText(
      overflow: textOverflow,
      textAlign: textAlign,
      maxLines: maxLines,
      maxFontSize: 8,
      minFontSize: 8,
      text ?? '',
      style: TextStyle(
        decoration: textDecoration,
        fontWeight: fontWeight,
        fontStyle: fontStyle,
        fontSize: fontSize,
        color: color,
      ),
    );
  }

