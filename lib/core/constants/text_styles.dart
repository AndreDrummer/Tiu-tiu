  static AutoSizeText autoSizeText8(
    String? text, {
    TextDecoration? textDecoration,
    TextOverflow? textOverflow,
    FontWeight? fontWeight,
    FontStyle? fontStyle,
    double? fontSize = 8,
    TextAlign? textAlign,
    int? maxLines,
    Color? color,
  }) {
    return AutoSizeText(
      maxFontSize: fontSize ?? 8,
      overflow: textOverflow,
      textAlign: textAlign,
      maxLines: maxLines,
      minFontSize: 8,
      text ?? '',
      style: TextStyle(
        decoration: textDecoration,
        fontWeight: fontWeight,
        fontStyle: fontStyle,
        fontSize: fontSize,
        color: color,
      ),
    );
